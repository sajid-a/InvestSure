        <h2>General Insurance</h2>
        	
 
In today's fast growing economy and consumerism, the need for insurances to
cover different types of risks have increased. Today apart from life coverage,
we have a different types of risk coverage including health insurance, travel
insurance, theft insurance and even wedding insurance, to name a few.
Responsible people adequately plan their insurance to cover different types of
risks.<br /><br />
Any insurance other than 'Life Insurance' falls under the classification of
General Insurance. It comprises of :-
<ul type="disc">
<li>Insurance of property against fire, theft, burglary, terrorism, natural
disasters etc.</li>
<li>Personal insurance such as Accident Policy, Health Insurance and liability
insurance which covers legal liabilities.</li>
<li>Errors and Omissions Insurance for professionals, credit insurance etc.</li>
<li>Policy covers such as coverage of machinery against breakdown or loss or
damage during the transit.</li>
<li>Policies that provide marine insurance covering goods in transit by sea, air,
railways, waterways and road and cover the hull of ships.</li>
<li>Insurance of motor vehicles against damages or accidents and theft.</li>
</ul>
General insurance products and services are offered as package policies
offering a combination of the covers mentioned above in various permutations
and combinations. There are package policies specially designed for
householders, shopkeepers, industrialists, agriculturists, entrepreneurs,
employees and for professionals such as doctors, engineers, chartered
accountants etc. Apart from standard covers, General insurance companies
also offer customized or tailor-made policies based on the personal
requirements of the customer.<br /><br />
Insurance in India used to be tightly regulated and monopolised by state-run
insurers. Following the move towards economic reform in the early 1990s,
various plans to revamp the sector finally resulted in the passage of the
Insurance Regulatory and Development Authority (IRDA) Act of 1999.
Significantly, the insurance business was opened on two fronts. Firstly,
domestic private-sector companies were permitted to enter both life and nonlife
insurance business. Secondly, foreign companies were allowed to
participate, albeit with a cap on shareholding at 26%.<br /><br />
There are totally 27 General Insurance (Non-Life) Companies in India. These
include 4 companies in Public Sector Undertakings and 23 companies in the
Private Sector.
 
