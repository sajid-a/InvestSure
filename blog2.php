 <h3>Health Insurance in India</h3>  
            <p>         
Health treatment nowadays is very costly. More than the disease it is the cost
of treatment that takes its toll. To get rid of health worries health / medical
insurance is the answer. But over 70 per cent of these spends are out of pocket
which leads to lot of hardships. According to a survey by NSSO (National
Sample Survey Organization), 40 per cent of the people hospitalized have
either had to borrow money or sell assets to cover their medical expenses.</p>
<p><b>It takes just one visit to a hospital to make us realize how vulnerable we are.</b>
It is a tough ordeal if you are diagnosed with an illness and need to be
hospitalized, no matter if you are rich or poor, male or female, young or old.
The list of lifestyle diseases like heart problems, diabetes, stroke, renal failure,
some cancers just seems to get longer and more common these days.
Thankfully there are more specialty hospitals and specialist doctors - but all
that comes at a cost. The super rich can afford such costs, but what about an
average middle class person? For an illness that requires hospitalization /
surgery, costs can easily run into 5 figures.</p>
<p>A Health Insurance Policy can cover such expenses to a large extent. Health
insurance policy not only covers expenses incurred during hospitalization but
also during the pre as well as post hospitalization stages like money spent for
conducting medical tests and buying medicines. The cover will be to the extent
of the sum insured.</p><br />
<h4>Types of Health Insurance...</h4>
There are mainly 3 types of Health Insurance covers which are as follows.
<ol><li><b>Individual Mediclaim</b><br />
The simplest form of health insurance is the Individual Mediclaim policy. It
covers the hospitalization expenses for an individual for upto the sum
assured limit.</li>
<li><b>Family Floater Policy</b><br />
Family Floater Policy is an enhanced version of the mediclaim policy. The
policy covers each family member and the entire family's expenses are
covered up to the sum assured limit. So, in many ways the family floater
plan offers flexibility in terms of utilizing the overall insurance coverage
among the group.</li>
<li><b>Unit Linked Health Plans</b><br />
Health Insurance Companies have introduced Unit Linked Health Plans which
combine health insurance with investment and pay back an amount at the end
of the insurance term. The returns are dependent on market performance.</li>
</ol><br />
<h4>Current Scenario in India...</h4><br /><p>
With a reach of just about 2% of the country's 1.2 billion population, India
offers a huge potential in health insurance market. There are over 30 health
insurance products in the category offered by both life and non-life insurers.
While ICICI Lombard, Bajaj Allianz and Reliance General are some of the
prominent general insurers in the health insurance space, Apollo DKV, Star
Health & Allied Insurance are the standalone players.
